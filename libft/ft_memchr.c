#include "libft.h"

void	*ft_memchr(const void *s1, int c, size_t n)
{
	unsigned int		i;
	const unsigned char	*str;

	i = 0;
	str = s1;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return ((void *)str + i);
		i++;
	}
	return (NULL);
}
