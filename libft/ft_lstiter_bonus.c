#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list *t;

	t = lst;
	if (t && f)
	{
		while (t != NULL)
		{
			f(t->content);
			t = t->next;
		}
	}
}
